import { FSWatcher, watch } from 'chokidar';
import path from 'path';
import fs from 'fs-extra';
import debounce from 'debounce';
import { isSafeFilename, toPercent } from 'web-build-utils';

/**
 * Options for Images2style
 *
 * @property {string} cwd - The working directory for relative paths. Defaults to process.cwd().
 * @property {string} src - The source directory containing images. Must be a directory.
 * @property {string} dest - The output file path for generated styles. Must be a file.
 * @property {function} [exclude] - Function to exclude files/directories from processing. Receives parsed path info and full path. Return true to exclude.
 * @property {function} [include] - Function to include files/directories for processing. Receives parsed path info and full path. Return true to include.
 * @property {function} [transform] - Function to transform generated CSS for each image. Receives CSS content, parsed path info, and full path. Returns transformed CSS string.
 * @property {number} [delay] - Debounce delay (ms) for packing after changes. Default is 200ms.
 * @property {boolean} [silent] - If true, suppresses plugin logging. Default is true.
 * @property {boolean} [watch] - If true, watch src and re-pack on changes. Default is false.
 */
export interface Images2styleOptions {
  cwd?: string;
  src: string;
  dest: string;
  exclude?: (srcInfo: path.ParsedPath, src: string) => boolean;
  include?: (srcInfo: path.ParsedPath, src: string) => boolean;
  transform?: (
    content: string,
    srcInfo: path.ParsedPath,
    src: string
  ) => string;
  delay?: number;
  silent?: boolean;
  watch?: boolean;
}

// Base stylesheet wrapper with a slot for generated rules.
const globalStyleTemplate = `/* This file was auto generated by images2style. */
.bg-full {
  background-size: 100% 100%;
}
{content}
`;

const imageStyleTemplate = `
.{className} {
  background-image: url('{imagePath}');
}
`;

const atlasStyleTemplate = `
.{className} {
  background: url('{imagePath}') {x}% {y}% / {w}% {h}% no-repeat;
}
`;

// Format percent values consistently for CSS output.
function toPercentStr(num: number): string {
  return toPercent(num, 4) + '';
}

/**
 * Plugin class for generating CSS styles from images.
 * Watches the source directory, processes images and atlas files, and writes CSS to the destination file.
 */
class Images2style {
  // Plugin options (fully resolved with defaults)
  private _options: Required<Images2styleOptions>;
  // File watcher instance
  private _watcher: FSWatcher | null = null;

  /**
   * Constructor
   * Validates and initializes plugin options.
   * Throws error if src is not a directory or dest is not a file.
   */
  constructor(options: Images2styleOptions) {
    if (path.extname(options.src)) {
      throw new Error('The options.src must be a directory.');
    }
    if (!path.extname(options.dest)) {
      throw new Error('The options.dest must be a file.');
    }

    this._options = {
      cwd: process.cwd(),
      exclude: () => false,
      include: () => true,
      transform: (content) => content,
      delay: 200,
      silent: true,
      watch: false,
      ...options,
    };
  }

  /**
   * Logs messages using console infrastructure logger if not silent.
   */
  private log(...args: any[]): void {
    if (this._options.silent) {
      return;
    }
    console.log(...args);
  }

  /**
   * Initializes and returns a file watcher for the source directory.
   * Calls the provided callback on file events.
   */
  private getWatcher(
    cb: (event: string, path: string, stats?: fs.Stats) => void
  ) {
    if (!this._watcher) {
      fs.ensureDirSync(this._options.src);
      this._watcher = watch('.', {
        cwd: this._options.src,
        ignoreInitial: true,
      });
      this._watcher.on('all', cb);
    }
    return this._watcher;
  }

  runPack() {
    const { src, dest, delay, watch } = this._options;
    const pack = async () => {
      const res = await this.pack(src);
      // Persist the combined CSS into the target file.
      await fs.outputFile(
        dest,
        globalStyleTemplate.replace('{content}', res.code)
      );
    };

    if (watch) {
      // Debounce to avoid re-packing on bursty file events.
      const delayPack = debounce(pack, delay);
      this.getWatcher((e, p) => {
        if (!isSafeFilename(p, true)) {
          return;
        }
        this.log(e, p);
        delayPack();
      });
    }

    return pack();
  }

  /**
   * Recursively scans the source directory for image files and generates CSS code for each.
   * If an atlas JSON file is found, generates CSS for spritesheet frames.
   * @param src Source directory
   * @param res Accumulator for generated CSS code
   */
  private async pack(src: string, res = { code: '' }) {
    this.log('pack', src);
    const { exclude, include } = this._options;
    const fullNames = await fs.readdir(src);
    await Promise.all(
      fullNames.map(async (fullName) => {
        const srcPath = path.join(src, fullName);
        const info = path.parse(srcPath);
        if (!isSafeFilename(srcPath, true) || exclude(info, srcPath)) {
          return;
        }
        if (info.ext) {
          // File
          // Only supported image extensions are turned into CSS.
          if (
            /^\.(jpe?g|png|gif|svg|webp|avif)$/i.test(info.ext) &&
            include(info, srcPath)
          ) {
            const code = await this.genStyle(info, srcPath);
            res.code += code;
          }
        } else {
          // Dir
          await this.pack(srcPath, res);
        }
      })
    );

    this.log('packed', src);
    return res;
  }

  /**
   * Generates CSS for a single image or atlas frame.
   * If a corresponding atlas JSON file exists, generates CSS for each frame in the atlas.
   * Otherwise, generates a simple background-image CSS rule for the image.
   * @param info Parsed path info for the image file
   * @param srcPath Full path to the image file
   */
  private async genStyle(info: path.ParsedPath, srcPath: string) {
    const { cwd, src, transform } = this._options;
    const baseDir = path.dirname(src);
    const srcDir = path.dirname(srcPath);
    // Class name is derived from the relative path + file name to avoid collisions.
    const className =
      path.relative(baseDir, srcDir).replace(/[\\/]+/g, '-') + '-' + info.name;
    // Use a tilde path so bundlers can resolve it from project root.
    const imagePath = `~/${path.relative(cwd, srcPath)}`;

    let content = '';
    const atlasPath = path.join(srcDir, info.name + '.json');
    if (fs.existsSync(atlasPath)) {
      const json = await fs.readJSON(atlasPath, { throws: false });
      if (json && json.meta && json.frames) {
        const { w: width, h: height } = json.meta.size;
        Object.keys(json.frames).forEach((name) => {
          const { x, y, w, h } = json.frames[name].frame;
          // Guard against zero-diff sizes to avoid NaN/Infinity output.
          const diffW = width - w;
          const diffH = height - h;
          const percentX = toPercentStr(diffW > 0 ? x / diffW : 0);
          const percentY = toPercentStr(diffH > 0 ? y / diffH : 0);
          const percentW = toPercentStr(width / w);
          const percentH = toPercentStr(height / h);
          content += atlasStyleTemplate
            .replace('{className}', className + '-' + name)
            .replace('{imagePath}', imagePath)
            .replace('{x}', percentX)
            .replace('{y}', percentY)
            .replace('{w}', percentW)
            .replace('{h}', percentH);
        });
      } else if (json) {
        this.log('Unsupported atlas file:', atlasPath);
      }
    }

    if (!content) {
      content = imageStyleTemplate
        .replace('{className}', className + info.ext.replace('.', '-'))
        .replace('{imagePath}', imagePath);
    }

    return transform(content, info, srcPath);
  }
}

export function images2style(options: Images2styleOptions) {
  return new Images2style(options).runPack();
}
